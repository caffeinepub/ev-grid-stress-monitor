// Duration multipliers: 6h = peak stress (higher load/transformer), 12h = medium, 24h = full-day avg
export function getStateValuesForDuration(
  stateName: string,
  duration: "6" | "12" | "24",
) {
  const d = STATE_DATA[stateName];
  if (!d) {
    const base = {
      gridLoad: 6800,
      transformer: 51,
      sessions: 375,
      evPop: 20000,
      renewable: 27,
      stations: 370,
      fastChargers: 92,
    };
    if (duration === "6")
      return {
        gridLoad: Math.round(base.gridLoad * 1.12),
        transformer: +(base.transformer * 1.1).toFixed(1),
        sessions: Math.round(base.sessions * 1.15),
        evPop: base.evPop,
        renewable: +(base.renewable * 0.95).toFixed(1),
        stations: base.stations,
        fastChargers: base.fastChargers,
      };
    if (duration === "12")
      return {
        gridLoad: Math.round(base.gridLoad * 1.05),
        transformer: +(base.transformer * 1.04).toFixed(1),
        sessions: Math.round(base.sessions * 1.07),
        evPop: base.evPop,
        renewable: +(base.renewable * 0.98).toFixed(1),
        stations: base.stations,
        fastChargers: base.fastChargers,
      };
    return base;
  }
  if (duration === "6") {
    return {
      gridLoad: Math.round(d.avgGridLoad * 1.12),
      transformer: +(d.avgTransformer * 1.1).toFixed(1),
      sessions: Math.round(d.avgSessions * 1.15),
      evPop: d.avgEvPop,
      renewable: +(d.avgRenewable * 0.95).toFixed(1),
      stations: d.avgStations,
      fastChargers: d.avgFastChargers,
    };
  }
  if (duration === "12") {
    return {
      gridLoad: Math.round(d.avgGridLoad * 1.05),
      transformer: +(d.avgTransformer * 1.04).toFixed(1),
      sessions: Math.round(d.avgSessions * 1.07),
      evPop: d.avgEvPop,
      renewable: +(d.avgRenewable * 0.98).toFixed(1),
      stations: d.avgStations,
      fastChargers: d.avgFastChargers,
    };
  }
  return {
    gridLoad: Math.round(d.avgGridLoad),
    transformer: +d.avgTransformer.toFixed(1),
    sessions: d.avgSessions,
    evPop: d.avgEvPop,
    renewable: +d.avgRenewable.toFixed(1),
    stations: d.avgStations,
    fastChargers: d.avgFastChargers,
  };
}
